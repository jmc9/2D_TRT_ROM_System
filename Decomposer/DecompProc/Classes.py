#==================================================================================================================================#
class Grid:
    def __init__(self,bnds,crds):
        self.bnds = bnds
        self.crds = crds

#==================================================================================================================================#
#
#==================================================================================================================================#
class Dimension:
    name = ''
    len  = 0
    def __init__(self,len=0,name=''):
        self.name = name
        self.len  = len

#==================================================================================================================================#
#
#==================================================================================================================================#
class DMD:
    def __init__(self,N_modes=0,eval=complex(0.,0.),evec=complex(0.,0.),expval=complex(0.,0.)):
        self.N_modes = N_modes
        self.eval = eval
        self.evec = evec
        self.expval = expval

#==================================================================================================================================#
#
#==================================================================================================================================#
class POD:
    U  = []
    S  = []
    Vt = []

#==================================================================================================================================#
#
#==================================================================================================================================#
class Data:

    name  = ''
    dat   = []
    grids = Grid([],[])
    dims  = Dimension()
    clen  = 0.
    rank  = 0.
    opt   = [0]

    def __init__(self,name):
        self.name  = name
        return

    def typeset(self,type):
        self.type = type

        if (type in ['DMD','DMDg']):
            self.dat = DMD
        elif (type in ['POD','PODg']):
            self.dat = POD
        else:
            print('Error! unsupported decomposition type detected')
            quit()
