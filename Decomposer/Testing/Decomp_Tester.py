def Options():
    try:
        opts, args = getopt.getopt(sys.argv[1:], 'ho:', ['help','output='])
    except getopt.GetoptError as err:
        print(str(err))
        usage()
        quit()

    Default_Opts()

    for o, a in opts:
        if o in ['-h','--help']:
            usage()
            quit()

    return

