#==================================================================================================================================#
def norm_inf(x):
    n = len(x)
    norm = 0.
    for i in range(n):
        if abs(x[i])>norm: norm = abs(x[i])

    return norm

#==================================================================================================================================#
#
#==================================================================================================================================#
def norm1(x):
    n = len(x)
    norm = 0.
    for i in range(n):
        norm = norm + abs(x[i])

    return norm

#==================================================================================================================================#
#
#==================================================================================================================================#
def normL1(x,A):
    n = len(x)
    norm = 0.
    for i in range(n):
        norm = norm + A[i]*abs(x[i])

    return norm

#==================================================================================================================================#
#
#==================================================================================================================================#
def norm2(x):
    n = len(x)
    norm = 0.
    for i in range(n):
        norm = norm + x[i]**2
    norm = np.sqrt(norm)

    return norm

#==================================================================================================================================#
#
#==================================================================================================================================#
def normL2(x,A):
    n = len(x)
    norm = 0.
    for i in range(n):
        norm = norm + A[i]*x[i]**2
    norm = np.sqrt(norm)

    return norm
